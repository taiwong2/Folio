import Foundation

public func mapPassagesToResults(_ passages: [RetrievedPassage], scoreFromBM25: (Double) -> Double = { $0 }) -> [RetrievedResult] {
    passages.map { p in
        RetrievedResult(
            sourceId: p.sourceId,
            startPage: p.startPage,
            excerpt: p.excerpt,
            text: p.text,
            bm25: p.bm25,
            cosine: nil,
            score: scoreFromBM25(p.bm25),
            citations: p.citations
        )
    }
}

public struct IndexingConfig: Sendable {
    public var useContextualPrefix = true
    public var contextFn: (@Sendable (_ doc: LoadedDocument, _ page: LoadedPage, _ chunk: String) async throws -> String)? = nil

    public init() {}
}

public struct FolioConfig {
    public var chunking = ChunkingConfig()
    public var indexing = IndexingConfig()
    public init() {}
}

public struct RetrievalFilter: Sendable, Hashable {
    public var sourceIds: Set<String>?
    public var fileTypes: Set<String>?
    public var pageRange: ClosedRange<Int>?
    public var tags: Set<String>?

    public init(sourceIds: Set<String>? = nil, fileTypes: Set<String>? = nil, pageRange: ClosedRange<Int>? = nil, tags: Set<String>? = nil) {
        self.sourceIds = sourceIds
        self.fileTypes = fileTypes
        self.pageRange = pageRange
        self.tags = tags
    }
}

public struct RetrievedPassage {
    public let sourceId: String
    public let startPage: Int?
    public let excerpt: String
    public let text: String
    public let bm25: Double
    public let citations: [Citation]
}

public struct RetrievedResult: Sendable {
    public let sourceId: String
    public let startPage: Int?
    public let excerpt: String
    public let text: String
    public let bm25: Double
    public let cosine: Double?
    public let score: Double
    public let citations: [Citation]
}

public struct Citation: Sendable, Hashable, Codable {
    public let sourceId: String
    public let sourceName: String
    public let fileType: String?
    public let page: Int?
    public let sectionTitle: String?
    public let chunkId: String
    public let parentId: String?
    public let excerpt: String?
}

/// Stored chunk exposed for debugging / inspection. Mirrors the internal store
/// row without leaking SQL-side types.
public struct InspectableChunk: Sendable, Hashable {
    public let chunkId: String
    public let page: Int?
    public let sectionTitle: String?
    public let text: String
    /// Contextual prefix generated by `IndexingConfig.contextFn` (or the
    /// rule-based fallback) and stored alongside the chunk. Empty when
    /// contextual prefixes are disabled. Prepended to `text` when computing
    /// FTS5 index content and embedding input, but kept separate on disk so
    /// the raw chunk content is preserved.
    public let contextPrefix: String

    public init(chunkId: String, page: Int?, sectionTitle: String?, text: String, contextPrefix: String = "") {
        self.chunkId = chunkId
        self.page = page
        self.sectionTitle = sectionTitle
        self.text = text
        self.contextPrefix = contextPrefix
    }
}

